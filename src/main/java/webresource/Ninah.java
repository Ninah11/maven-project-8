public class Ninah {

    private String name;
    private double amount;
    private int mutoni;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public int getMutoni() {
        return mutoni;
    }

    public void setMutoni(int mutoni) {
        this.mutoni = mutoni;
    }

}
